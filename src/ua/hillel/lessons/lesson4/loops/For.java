package ua.hillel.lessons.lesson4.loops;

public class For {
    public static void main(String[] args) {
        forExample1();
    }

    public static void forExample1() {
        for (int i = 1; i <= 5; i++) {
            System.out.println("Maks");
        }
    }
    public static void forExample2() {
        for (int i = 3; i >= 3; i--) {
            System.out.println(i);
        }
    }


}
